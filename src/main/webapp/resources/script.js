   function exibirForm() {
        var form = document.querySelector("#Form1");
        form.style.display = 'block';

        var button = document.querySelector("#botao");
        button.style.display = 'none';

        var retorno = document.querySelector("#retorno");
        retorno.style.display = 'none';
    }

    function ocultarForm() {
        // Ocultar o formulário e exibir o retorno
        var form = document.querySelector("#idCarro");
        form.style.display = 'none';

        var retorno = document.querySelector("#retorno");
        retorno.style.display = 'block';
    }

